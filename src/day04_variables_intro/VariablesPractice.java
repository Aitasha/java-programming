package day04_variables_intro;

public class VariablesPractice {
    public static void main(String[] args){
        //declare variable students that stores number
        int students;//declare once and we can keep reusing it
        students = 50; //assign value
        System.out.println(students);
        System.out.println(students);
        students = 70;//reassigning value
        System.out.println(students);


        students = 111;
        students = 125;
        System.out.println(students);

        int teachers;
        teachers = 2;
        int mentors;
        mentors = 25;
        System.out.print("Number of Teachers:");
        System.out.println(teachers);//2
        System.out.print("Mentors:");
        System.out.println(mentors);
        System.out.println(students);







    }
}
