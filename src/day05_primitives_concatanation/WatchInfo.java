package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args){
        String brand = "Cartier";
        String color = "Camel";
        Double price = 10000.0;
        boolean waterResistant = false;
        boolean isSmart = false;
        char gender = 'F';


        System.out.println("Brand:\t\t\t\t\t" + brand);
        System.out.println("Color:\t\t\t\t\t" + color);
        System.out.println("Price:\t\t\t\t\t" + price);
        System.out.println("Is water resistant?:\t" + waterResistant);
        System.out.println("Is smart?:\t\t\t\t" + isSmart);
        System.out.println("Gender:\t\t\t\t\t" + gender);

    }
}
